Witamy! Dziękujemy za skorzystanie z naszej aplikacji. To dobry wybór!<br />
Żeby zacząć zarządzać swoim budżetem musisz stworzyć swój pierwszy <strong class="watermark">Wirtualny portfel</strong><span style="font-size:11px;">&copy;</span>.<br />
To proste - wystarczy nazwać swój portfel (np. sierpień 2012, od daty do daty, wakacje, itd...) a następnie wprowadzić pierwszy przychód do portfela (zawsze będzie go można uzupełnić w trakcie).<br />
Każdy portfel reprezentuje oddzielny okres rozliczeniowy. Może to być miesiąc, tydzień, rok. Sam zdecydujesz kiedy zamkniesz budżet na określony okres i "zaksięgujesz" zyski (oszczędności) lub straty (wydatki ponad wkład).
<br /><br />
Przykładem może być postępowanie podyktowane mniej lub bardziej regularną wypłatą: w dniu otrzymania wypłaty zakładamy portfel o nazwie np. <em>"maj 2013"</em>.
Wprowadzamy kwotę jaką dostaliśmy do portfela i w miarę upływu miesiąca i naszej gotówki zapisujemy historię naszych wydatków.<br />
W taki sposób będziesz w przejrzysty sposób możesz widzieć na co ile wydajesz i ile ewentualnie oszczędzasz.<br />
Kiedy miesiąc się skończy a nowa wypłata pojawi się na Twoim koncie wystarczy zakończyć portfel z maja (zapisane wtedy będą zyski lub straty) stworzyć nowy i dalej obserwować co się dzieje z Twoimi zarobkami.
<br /><br />
